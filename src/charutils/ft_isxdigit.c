#include "libft.h"

// Check if the character is a hexadecimal digit (0-9, a-f, A-F).
int	ft_isxdigit(int c)
{
	return ((c >= '0' && c <= '9') \
			|| (c >= 'a' && c <= 'f') \
			|| (c >= 'A' && c <= 'F'));
}
